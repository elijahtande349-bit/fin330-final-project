all_tickers = tickers + ["SPY"]
raw = yf.download(all_tickers, period="1y", auto_adjust=False)

# Pull just the closing prices for all tickers
prices = raw["Close"]
prices.head()

# Step 4: Calculate daily returns
# pct_change() gives the % change from one day to the next

returns = prices.pct_change().dropna()

# Separate stock returns from benchmark
stock_returns = returns[tickers]
spy_returns = returns["SPY"]

# Calculate portfolio daily return
# Each stock's return is multiplied by its weight, then summed
# This gives us one daily return number for the whole portfolio
portfolio_returns = (stock_returns * weights).sum(axis=1)

# Step 5: Performance Metrics
# Total return: how much $1 grew over the year
# (1 + daily return) compounded every day

total_return = (1 + portfolio_returns).prod() - 1
benchmark_return = (1 + spy_returns).prod() - 1

print(f"Portfolio Total Return:  {total_return * 100:.2f}%")
print(f"Benchmark (SPY) Return:  {benchmark_return * 100:.2f}%")
print(f"Outperformance:          {(total_return - benchmark_return) * 100:.2f}%")

# Annualized volatility (same formula as Part 1)
port_volatility = portfolio_returns.std() * np.sqrt(252) * 100
print(f"Portfolio Volatility:    {port_volatility:.2f}%")

# Sharpe Ratio: return per unit of risk
# Higher = better. Above 1.0 is generally considered good
# We assume a 0% risk-free rate to keep it simple
sharpe = (portfolio_returns.mean() / portfolio_returns.std()) * np.sqrt(252)
print(f"Sharpe Ratio:            {sharpe:.2f}")

# Step 6: Plot portfolio vs benchmark over time
# Cumulative return shows the growth of $1 invested on day 1

cumulative_portfolio = (1 + portfolio_returns).cumprod()
cumulative_spy = (1 + spy_returns).cumprod()

plt.figure(figsize=(10, 6))
plt.plot(cumulative_portfolio, label="My Portfolio")
plt.plot(cumulative_spy, label="SPY Benchmark")
plt.legend()
plt.title("Portfolio vs. SPY - 1 Year Cumulative Return")
plt.ylabel("Growth of $1")
plt.show()
