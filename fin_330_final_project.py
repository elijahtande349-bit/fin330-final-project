# Step 3: RSI (Relative Strength Index)
# RSI measures momentum - how fast prices are moving up vs down
# It ranges from 0 to 100
# Above 70 = overbought (might drop), Below 30 = oversold (might rise)

# Calculate daily price changes
delta = close.diff()

# Separate gains (positive changes) and losses (negative changes)
gains = delta.clip(lower=0)
losses = -delta.clip(upper=0)

# Average gains and losses over 14 days
avg_gain = gains.rolling(14).mean()
avg_loss = losses.rolling(14).mean()

# RS = average gain divided by average loss
# RSI formula converts that into a 0-100 scale
rs = avg_gain / avg_loss
rsi = 100 - (100 / (1 + rs))

# Get the most recent RSI value
current_rsi = rsi.iloc[-1]
print(f"Current RSI: {current_rsi:.2f}")

